    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String dniBuscar = JOptionPane.showInputDialog(this, "🔍 Ingresa el DNI del Cliente a buscar:");

        if (dniBuscar == null || dniBuscar.trim().isEmpty()) {
            return;
        }

        int filaEncontrada = -1;

        for (int i = 0; i < tbCliente.getRowCount(); i++) {
            String dniTabla = tbCliente.getValueAt(i, 2).toString();

            if (dniTabla.equalsIgnoreCase(dniBuscar.trim())) {
                filaEncontrada = i;
                break;
            }
        }

        if (filaEncontrada != -1) {
            tbCliente.setRowSelectionInterval(filaEncontrada, filaEncontrada);
            tbCliente.scrollRectToVisible(tbCliente.getCellRect(filaEncontrada, 0, true));
        } else {
            JOptionPane.showMessageDialog(this, "❌ No se encontró el motorizado con DNI: " + dniBuscar);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

