    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = txtNombre.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String direccion = txtDireccion.getText().trim();
        boolean tieneConvenio = rbtSi.isSelected();

        Restaurante nuevo = new Restaurante(nombre, telefono, direccion, tieneConvenio);
        ControladorRestaurante controlador = new ControladorRestaurante();
        if (controlador.registrarRestaurante(nuevo)) {
            JOptionPane.showMessageDialog(this, "✅ Restaurante registrado correctamente.");

            if (padre != null) {
                padre.cargarTabla();
            }

            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "❌ Verifica los datos ingresados.");

        }


    }//GEN-LAST:event_jButton1ActionPerformed

